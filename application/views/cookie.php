<h1>Penggunaan Cookie</h1>

<h2>Jenis Cookie yang Kami Gunakan</h2>

<p>Cookie dapat berupa Cookie &quot;Persisten&quot; atau &quot;Sesi&quot;. Cookie Persisten tetap ada di komputer pribadi atau perangkat seluler Anda saat Anda offline, sementara Cookie Sesi dihapus segera setelah Anda menutup browser web Anda.</p>

<p>Kami menggunakan Cookie sesi dan persisten untuk tujuan yang ditetapkan di bawah ini:</p>

<ul>
	<li>
	<p><strong>Cookie yang Diperlukan / Esensial</strong></p>

	<p>Jenis: Cookie Sesi</p>

	<p>Dikelola oleh: Kami</p>

	<p>Tujuan: Cookie ini penting untuk memberikan Anda layanan yang tersedia melalui Situs Web dan untuk memungkinkan Anda menggunakan beberapa fiturnya. Mereka membantu untuk mengotentikasi pengguna dan mencegah penipuan penggunaan akun pengguna. Tanpa Cookie ini, layanan yang Anda minta tidak dapat diberikan, dan Kami hanya menggunakan Cookie ini untuk menyediakan layanan tersebut kepada Anda.</p>
	</li>
	<li>
	<p><strong>Cookie Fungsionalitas</strong></p>

	<p>Jenis: Cookie Persisten</p>

	<p>Dikelola oleh: Kami</p>

	<p>Tujuan: Cookie ini memungkinkan kami untuk mengingat pilihan yang Anda buat saat Anda menggunakan Situs Web, seperti mengingat detail login atau preferensi bahasa Anda. Tujuan dari Cookie ini adalah untuk memberikan Anda pengalaman yang lebih pribadi dan untuk menghindari Anda harus memasukkan kembali preferensi Anda setiap kali Anda menggunakan Situs Web.</p>
	</li>
</ul>

