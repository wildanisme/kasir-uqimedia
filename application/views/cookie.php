<h1>Kebijakan Cookie</h1>

<p>Terakhir diperbarui: 19 April 2022</p>

<p>Kebijakan Cookie ini menjelaskan apa itu Cookie dan bagaimana Kami menggunakannya. Anda harus membaca kebijakan ini sehingga Anda dapat memahami jenis cookie yang Kami gunakan, atau informasi yang Kami kumpulkan menggunakan Cookie dan bagaimana informasi tersebut digunakan. Kebijakan Cookies ini dibuat dengan bantuan <a href="https://www-privacypolicies-com.translate.goog/cookies-policy-generator/?_x_tr_sl=auto&amp;_x_tr_tl=id&amp;_x_tr_hl=id&amp;_x_tr_pto=wapp" target="_blank">Pembuat Kebijakan Cookies</a> .</p>

<p>Cookie biasanya tidak berisi informasi apa pun yang secara pribadi mengidentifikasi pengguna, tetapi informasi pribadi yang kami simpan tentang Anda mungkin terkait dengan informasi yang disimpan di dan diperoleh dari Cookie. Untuk informasi lebih lanjut tentang cara Kami menggunakan, menyimpan, dan menjaga keamanan data pribadi Anda, lihat Kebijakan Privasi kami.</p>

<p>Kami tidak menyimpan informasi pribadi yang sensitif, seperti alamat surat, kata sandi akun, dll. dalam Cookie yang Kami gunakan.</p>

